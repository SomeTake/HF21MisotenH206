#define _PLACECONFIG_H_

namespace Field::Model
{
	/**************************************
	PlaceModelのタイプを表す列挙子
	***************************************/
	enum PlaceType
	{
		None,			//初期状態
		Road,			//道
		Town,			//街
		River,			//川
		Bridge,			//橋
		Junction,		//交差点
		Moutain			//山
	};

	/**************************************
	隣接方向を表す列挙子
	***************************************/
	enum Adjacency
	{
		Back,			//Zマイナス方向の隣接
		Left,			//Xマイナス方向の隣接
		Forward,		//Zプラス方向の隣接
		Right,			//Xプラス方向の隣接
		Max
	};
}
